function getTier(wr) {
  if (wr >= 52) return 'S';
  if (wr >= 50.5) return 'A';
  if (wr >= 49.5) return 'B';
  return 'C';
}

function processData(rawData, role) {
  return rawData.map(item => {
    const mapInfo = championMap[item.cn] || { en: "Unknown", ja: item.cn };
    return {
      champion_name: mapInfo.ja,
      champion_name_en: mapInfo.en,
      win_rate: item.wr,
      pick_rate: item.pr,
      ban_rate: item.br,
      tier: getTier(item.wr),
      role: role
    };
  });
}

async function run() {
  const topRecords = processData(topData, 'TOP');
  const jungleRecords = processData(jungleData, 'JUNGLE');
  const midRecords = processData(midData, 'MID');
  const adcRecords = processData(adcData, 'ADC');
  const supRecords = processData(supData, 'SUPPORT');
  
  const allRecords = [...topRecords, ...jungleRecords, ...midRecords, ...adcRecords, ...supRecords];

  console.log('Deleting old champion_stats...');
  await supabase.from('champion_stats').delete().neq('id', -1); // delete all

  console.log('Inserting new stats...');
  const { error } = await supabase.from('champion_stats').insert(allRecords);
  
  if (error) {
    console.error('Error inserting data:', error);
  } else {
    console.log('Successfully updated tier lists for all roles!');
  }
}

run();
